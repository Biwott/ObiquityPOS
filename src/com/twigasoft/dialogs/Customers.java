    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (jComboBox1.getSelectedIndex() == 0 && custId == -1) {
            if (validateNewEntry(txtFirstName, txtLastName)) {
                saveEntry();
                FormUtils.showSuccessMessage(lblSuccess, "Succesfully Updated");
                btnSave.setText("Save");
            }

        } else if (jComboBox1.getSelectedIndex() == 1 && custId == -1) {
            if (validateNewEntry(txtCorporate)) {
                saveEntry();
                FormUtils.showSuccessMessage(lblSuccess, "Succesfully Updated");
                btnSave.setText("Save");
            }

        } else if (jComboBox1.getSelectedIndex() == 0 && custId != -1) {
            if (validateUpdateEntry(txtFirstName, txtLastName)) {
                UpdateEntry();
                clearFields();
                FormUtils.showSuccessMessage(lblSuccess, "Succesfully Updated");
                btnSave.setText("Save");
                btnCancel.doClick();
                custId = -1;
                jComboBox1.setEnabled(true);
            }

        } else if (jComboBox1.getSelectedIndex() == 1 && custId != -1) {
            if (validateUpdateEntry(txtCorporate)) {
                UpdateEntry();
                clearFields();
                FormUtils.showSuccessMessage(lblSuccess, "Succesfully Updated");
                btnSave.setText("Save");
                btnCancel.doClick();
                custId = -1;
                jComboBox1.setEnabled(true);
            }

        }
        model.refresh(column, table);
    }//GEN-LAST:event_btnSaveActionPerformed

