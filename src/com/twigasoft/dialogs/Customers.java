    public void saveEntry() {
        Connection conn = null;
        PreparedStatement pst = null;
        String sql = "INSERT INTO tbl_customers VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
        try {
            conn = Database.getConnection();
            pst = conn.prepareStatement(sql);
            pst.setInt(1, 0);
            pst.setString(2, txtFirstName.getText());
            pst.setString(3, txtLastName.getText());
            pst.setString(4, txtEmail.getText());
            pst.setBoolean(5, chkTax.isSelected());
            pst.setString(6, txtContacts.getText());
            pst.setString(7, txtTown.getText());
            pst.setString(8, txtAreaComments.getText());
            pst.setInt(9, 1);//create
            pst.setObject(10, new Timestamp(new Date().getTime()));
            pst.setInt(11, 1);//update
            pst.setObject(12, new Timestamp(new Date().getTime()));
            pst.setString(13, txtAddress.getText());
            pst.setString(14, txtPostalCode.getText());
            pst.setString(15, txtCorporate.getText());
            pst.executeUpdate();
            clearFields();
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } finally {
            try {
                pst.close();
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(Customers.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    public void UpdateEntry() {
        Connection conn = null;
        PreparedStatement pst = null;
        String sql = "UPDATE tbl_customers SET  corporate_name=?, "
                + "first_name = ?,last_name = ?,contacts = ?,"
                + "email = ?,address = ?, post_code=?, town=?,"
                + " tax_exempt=?, notes=? WHERE id =?";
        try {
            conn = Database.getConnection();
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtCorporate.getText());
            pst.setString(2, txtFirstName.getText());
            pst.setString(3, txtLastName.getText());
            pst.setString(4, txtContacts.getText());
            pst.setString(5, txtEmail.getText());
            pst.setString(6, txtAddress.getText());
            pst.setString(7, txtPostalCode.getText());
            pst.setString(8, txtTown.getText());
            pst.setBoolean(9, chkTax.isSelected());
            pst.setString(10, txtAreaComments.getText());
            pst.setInt(11, custId);
            pst.executeUpdate();
            clearFields();
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } finally {
            try {
                pst.close();
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(Customers.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

