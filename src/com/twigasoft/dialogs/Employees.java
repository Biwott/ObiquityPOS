    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String name1 = null;
        String name2 = null;
        if (jTable1.getSelectedRowCount() > 0) {
            name1 = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
            name2 = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
            int reply = JOptionPane.showConfirmDialog(this,
                    "<html>Delete Employee <b>&rsquo;"
                    + name1 + " " + name2
                    + "&rsquo;</b> ?</html>",
                    "Confirm Delete!", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                DataUtils.deleteRow("tbl_employee", "first_name", "last_name", name1, name2);
                model.refresh(column, table);
            }
            name1 = null;
            name2 = null;
        }
        if (jTable1.getSelectedRow() >= 0) {
            btnDelete.setEnabled(true);
            btnModify.setEnabled(true);
        } else {
            btnDelete.setEnabled(false);
            btnModify.setEnabled(false);
        }

    }//GEN-LAST:event_btnDeleteActionPerformed
