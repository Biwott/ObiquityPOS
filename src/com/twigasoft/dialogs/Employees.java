    public void updateEntry() {
        Connection conn = null;
        PreparedStatement pst = null;
        String sql = "UPDATE tbl_employees SET  first_name=?, "
                + "last_name = ?, dob = ?, contacts = ?, "
                + "national_id = ?, gender = ?, address=?, town=?, "
                + "residence=?, email=?, staff_no=?, post_code=? WHERE id =?";
        try {
            conn = Database.getConnection();
            pst = conn.prepareStatement(sql);

            pst.setString(1, txtFirstName.getText());
            pst.setString(2, txtLastName.getText());
            pst.setString(3, ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText());
            pst.setString(4, txtContacts.getText());
            pst.setString(5, txtNatId.getText());
            pst.setString(6, rdoMale.isSelected() ? "Male" : "Female");
            pst.setString(7, txtAddress.getText());
            pst.setString(8, txtTown.getText());
            pst.setString(9, txtResidence.getText());
            pst.setString(10, txtEmail.getText());
            pst.setString(11, txtStaffNo.getText());
            pst.setString(12, txtPostCode.getText());
            pst.setInt(13, empId);
            pst.executeUpdate();
            FormUtils.showSuccessMessage(lblSuccess, "Succesfully Added!");
            clearFields();
            btnCancel.doClick();
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } finally {
            try {
                pst.close();
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    public void saveEntry() {
        Connection conn = null;
        PreparedStatement pst = null;
        String sql = "INSERT INTO tbl_employees VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
        try {
            conn = Database.getConnection();
            pst = conn.prepareStatement(sql);
            pst.setInt(1, 0);
            pst.setString(2, txtFirstName.getText());
            pst.setString(3, txtLastName.getText());
            pst.setString(4, ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText());
            pst.setString(5, txtContacts.getText());
            pst.setString(6, txtNatId.getText());
            pst.setString(7, rdoMale.isSelected() ? "Male" : "Female");
            pst.setString(8, txtAddress.getText());
            pst.setString(9, txtTown.getText());
            pst.setString(10, txtResidence.getText());
            pst.setString(11, txtEmail.getText());
            pst.setInt(12, 1);//create
            pst.setObject(13, new Timestamp(new Date().getTime()));
            pst.setInt(14, 1);//update
            pst.setObject(15, new Timestamp(new Date().getTime()));
            pst.setString(16, txtStaffNo.getText());
            pst.setString(17, txtPostCode.getText());
            pst.executeUpdate();
            FormUtils.showSuccessMessage(lblSuccess, "Succesfully Added!");
            clearFields();
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } finally {
            try {
                pst.close();
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    public boolean validateNewEntry(JTextField field1, JTextField field2) {
        if (field1.getText().equals("")) {
            FormUtils.blinkErrorMesage(lblError, field1, "* First Name Cannot be Empty!");
            return false;
        } else if (field2.getText().equals("")) {
            FormUtils.blinkErrorMesage(lblError, field2, "* Last Name Cannot be Empty!");
            return false;
        } else if (DataUtils.checkExistance2("tbl_employees", field1.getText(), field2.getText())) {
            JOptionPane.showMessageDialog(this, "<html>Duplicate Entry " + "<b>&rsquo;" + field1.getText() + " " + field2.getText() + "&rsquo;</b> !</html>");
            return false;
        }
        return true;
    }

    public boolean validateUpdateEntry(JTextField field1, JTextField field2) {
        if (field1.getText().equals("")) {
            FormUtils.blinkErrorMesage(lblError, field1, "* First Name Cannot be Empty!");
            return false;
        } else if (field2.getText().equals("")) {
            FormUtils.blinkErrorMesage(lblError, field2, "* Last Name Cannot be Empty!");
            return false;
        }
        return true;
    }

