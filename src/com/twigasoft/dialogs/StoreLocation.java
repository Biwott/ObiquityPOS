    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String name = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
        int reply = JOptionPane.showConfirmDialog(this, "<html>Delete  Category <b>&rsquo;" + name + "&rsquo;</b> ?</html>", "Confirm Delete!", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {

            DataUtils.deleteRow("tbl_store_locations", "name", name);
            model.refresh(column, table);

        }
        if (jTable1.getSelectedRow() >= 0) {
            btnBack.doClick();
            btnDelete.setEnabled(true);
            btnModify.setEnabled(true);
        } else {
            btnDelete.setEnabled(false);
            btnModify.setEnabled(false);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

