    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Connection conn = null;
        PreparedStatement pst = null;

        if (catId == -1 && validateNewEntry(txtName) && validateUpdateEntry(txtCode)) {
            String sql = "INSERT INTO tbl_store_locations(name, code, created_by, time_created) VALUES(?,?,?,?)";
            try {

                conn = Database.getConnection();
                pst = conn.prepareStatement(sql);
                pst.setString(1, txtName.getText());
                pst.setString(2, txtCode.getText());
                pst.setInt(3, app.getEmpId());
                pst.setObject(4, new Timestamp(new Date().getTime()));
                pst.executeUpdate();
                FormUtils.showSuccessMessage(lblSuccess, "Successfully Added!");;
                txtName.setText("");
                txtCode.setText("");

            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } finally {
                try {
                    pst.close();
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(StoreLocation.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            model.refresh(column, table);
        } else if (catId != -1 && validateUpdateEntry(txtName)) {
            String sql = "UPDATE tbl_store_locations SET name = ?, code=?, time_updated=? WHERE id =?";
            try {
                conn = Database.getConnection();
                pst = conn.prepareStatement(sql);
                pst.setString(1, txtName.getText());
                pst.setString(2, txtCode.getText());
                pst.setObject(3, new Timestamp(new Date().getTime()));
                pst.setInt(4, catId);
                pst.executeUpdate();
                FormUtils.showSuccessMessage(lblSuccess, "Successfully Updated!");
                txtName.setText("");
                txtCode.setText("");
                btnSave.setText("Save");
                btnBack.doClick();
                catId = -1;
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } finally {
                try {
                    pst.close();
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(StoreLocation.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
            model.refresh(column, table);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

