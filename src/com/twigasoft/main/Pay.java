    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (getTextField().getText().equals("0.00")) {
            JOptionPane.showMessageDialog(this, "No Payments Made!", "", 1);
            getTextField().requestFocus();
        } else if (NumUtils.removeSignInt(getTextField().getText(), ',') >= total) {
            saveSoldItems();
            app.btnCancel.setEnabled(false);
            app.txtSearch.setEnabled(false);
            app.tblMain.setEnabled(false);
            app.btnTender.setEnabled(false);
            app.btnHold.setEnabled(false);
            app.btnNew.requestFocus();
            app.blinkChange(getChangeField().getText());
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "The Goods Have Been Underpaid!", "", 1);
            getTextField().requestFocus();
        }
        app.editable = false;

    }//GEN-LAST:event_btnSaveActionPerformed

