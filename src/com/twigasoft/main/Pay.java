    public Object[][] getTableData(JTable table) {
        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        int nRow = dtm.getRowCount(), nCol = dtm.getColumnCount();
        Object[][] tableData = new Object[nRow][nCol];
        for (int i = 0; i < nRow; i++) {
            for (int j = 0; j < nCol; j++) {
                tableData[i][j] = dtm.getValueAt(i, j);
            }
        }
        return tableData;
    }

    public void createTransaction(int payMode) {
        Connection conn = null;
        PreparedStatement pst = null;
        String sql = "INSERT INTO tbl_sales_transactions"
                + "(receipt_no, till_no, emp_id, overall_discount, "
                + "payment_mode, total, paid, create_time) VALUES(?,?,?,?,?,?,?,?)";
        try {
            conn = Database.getConnection();
            pst = conn.prepareStatement(sql);
            pst.setInt(1, receipt_no);//receipt no
            pst.setInt(2, app.getTillNo());//till no
            pst.setInt(3, app.getEmpId());
            pst.setInt(4, NumUtils.removeSignInt(app.txtDisc.getText(), '%'));//overall disc
            pst.setInt(5, payMode);//pay mode
            pst.setInt(6, total);//total
            pst.setInt(7, paid);//paid
            pst.setObject(8, new Timestamp(new Date().getTime()));//create_time
            pst.executeUpdate();
        } catch (SQLException e) {
             Logger.getLogger(Pay.class.getName()).log(Level.SEVERE, null, e);
        }
        try {
            finalize();
        } catch (Throwable ex) {
            Logger.getLogger(Pay.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void storeItems(Object[][] obj) {
        Connection conn = null;
        PreparedStatement pst = null;
        String sql = "INSERT INTO tbl_sales_sold_items"
                + "(upc_code, description, qty, discount, subtotal, receipt_id) VALUES(?,?,?,?,?,?)";
        int receipt_id = DataUtils.getReceiptId(receipt_no);
        try {
            conn = Database.getConnection();
            pst = conn.prepareStatement(sql);
            for (int i = 0; i < obj.length; i++) {
                pst.setObject(1, obj[i][0]);
                pst.setObject(2, obj[i][1]);
                pst.setObject(3, obj[i][2]);
                pst.setObject(4, NumUtils.removeSignInt(obj[i][3].toString(), '%'));
                pst.setObject(5, NumUtils.removeSignInt(obj[i][5].toString(), ','));
                pst.setObject(6, receipt_id);
                pst.executeUpdate();
            }
        } catch (SQLException e) {
                Logger.getLogger(Pay.class.getName()).log(Level.SEVERE, null, e);
        }
        try {
            finalize();
        } catch (Throwable ex) {
            Logger.getLogger(Pay.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

