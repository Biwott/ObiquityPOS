    public final void populateTable(int receipt_no) {
        Connection conn = null;
        PreparedStatement pst = null;
        ResultSet rs = null;
        String sql = "SELECT * FROM tbl_sales_sold_items WHERE receipt_no = ? ";
        try {
            conn = Database.getConnection();
            pst = conn.prepareStatement(sql);
            pst.setInt(1, receipt_no);
            rs = pst.executeQuery();
            while (rs.next()) {
                model.insertRow(jTable1.getRowCount(),
                        new Object[]{rs.getObject(2), rs.getObject(3), rs.getObject(2), rs.getObject(5), rs.getObject(6)});
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
        try {
            finalize();
        } catch (Throwable ex) {
            Logger.getLogger(PastSales.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

