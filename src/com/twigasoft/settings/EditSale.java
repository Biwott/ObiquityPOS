    private void btnVoidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVoidActionPerformed
        if (jTable1.getForeground().equals(new Color(0, 0, 0))) {
            int i = JOptionPane.showConfirmDialog(this, "Do you want to Void this Sale?", "Void Sale", JOptionPane.OK_CANCEL_OPTION);
            if (i == JOptionPane.OK_OPTION) {
                jTable1.setForeground(new java.awt.Color(255, 0, 0));
                jTable1.setFont(new java.awt.Font("Tahoma", 1, 12));
                btnVoid.setText("Cancel Void");
                btnReprint.setEnabled(false);
                btnReturn.setEnabled(false);
                btnCancel.setText("Save & Exit");
            }
        } else {
            jTable1.setForeground(new java.awt.Color(0, 0, 0));
            jTable1.setFont(new java.awt.Font("Tahoma", 0, 12));
            btnVoid.setText("Void Sale");
            btnReprint.setEnabled(true);
            btnReturn.setEnabled(true);
            btnCancel.setText("Cancel");
        }
    }//GEN-LAST:event_btnVoidActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        model.setColumnCount(5);
        if (isReturn) {
            btnVoid.setEnabled(true);
            btnReprint.setEnabled(true);
            btnReturn.setText("Return Goods");
            model.fireTableDataChanged();
            isReturn = false;
        } else {
            dispose();
        }
    }//GEN-LAST:event_btnCancelActionPerformed

    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
        if (jTable1.getColumnCount() == 5) {
            model.addColumn("");
            FormUtils.setWidthAsPercentages(jTable1, 0.12, 0.40, 0.05, 0.05, 0.12, 0.06);
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                jTable1.setValueAt(" Return", i, 5);
            }
            isReturn = true;
            btnVoid.setEnabled(false);
            btnReprint.setEnabled(false);
            btnReturn.setText("Save Changes");
            jTable1.getColumnModel().getColumn(5).setCellRenderer(new EditSaleRenderer());
            jTable1.getColumnModel().getColumn(5).setCellEditor(new EditSaleEditor());
            model.fireTableDataChanged();
        } else {
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                if (jTable1.getValueAt(i, 5).equals(" Cancel")) {
                    model.removeRow(i);
                }

            }
            btnCancel.doClick();
        }

    }//GEN-LAST:event_btnReturnActionPerformed

