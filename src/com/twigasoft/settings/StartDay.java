    private void txtStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtStartActionPerformed
        calculateTotal();
        Connection conn = null;
        PreparedStatement pst = null;
        String sql = "INSERT INTO tbl_startday VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
        try {
            conn = Database.getConnection();
            pst = conn.prepareStatement(sql);
            pst.setInt(1, 0);
            pst.setInt(2, Integer.parseInt(txt1000.getText()));
            pst.setInt(3, Integer.parseInt(txt500.getText()));
            pst.setInt(4, Integer.parseInt(txt200.getText()));
            pst.setInt(5, Integer.parseInt(txt100.getText()));
            pst.setInt(6, Integer.parseInt(txt50.getText()));
            pst.setInt(7, Integer.parseInt(txt40.getText()));
            pst.setInt(8, Integer.parseInt(txt20.getText()));
            pst.setInt(9, Integer.parseInt(txt10.getText()));
            pst.setInt(10, Integer.parseInt(txt5.getText()));
            pst.setInt(11, Integer.parseInt(txt1.getText()));
            pst.setInt(12, NumUtils.removeSignInt(txtTotal.getText(), ','));
            pst.setInt(13, app.getEmpId());
            pst.setObject(14, new Timestamp(new Date().getTime()));
            pst.setInt(15, app.getTillNo());
            pst.executeUpdate();
            dispose();
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                pst.close();
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(StartDay.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_txtStartActionPerformed
