    public void calculateTotal() {
        int total = 0;
        total = (Integer.parseInt(txt1000.getText()) * 1000) + (Integer.parseInt(txt500.getText()) * 500)
                + (Integer.parseInt(txt200.getText()) * 200) + (Integer.parseInt(txt100.getText()) * 100)
                + (Integer.parseInt(txt50.getText()) * 50) + (Integer.parseInt(txt40.getText()) * 40)
                + (Integer.parseInt(txt20.getText()) * 20) + (Integer.parseInt(txt10.getText()) * 10)
                + (Integer.parseInt(txt5.getText()) * 5) + (Integer.parseInt(txt1.getText()) * 1);
        txtTotal.setText(NumUtils.formatAmount(Integer.toString(total)));

    }

