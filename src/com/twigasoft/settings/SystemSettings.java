    /**
     * Get current changes and save them into the properties file.
     */
    public void saveNewProperties() {
        try {
            Properties props = new Properties();
            props.setProperty("db.database", cmbDatabase.getSelectedItem().toString());
            props.setProperty("db.classname", txtDriverClass.getText());
            props.setProperty("db.host", txtDriverUrl.getText());
            props.setProperty("db.library", txtDriverLib.getText());
            props.setProperty("db.username", txtUserName.getText());
            props.setProperty("db.password", new String(txtPassword.getPassword()));
            props.setProperty("system.locale", cmbLocale.getSelectedItem().toString());
            props.setProperty("currency.name", cmbCurrencySym.getSelectedItem().toString());
            props.setProperty("currency.format", cmbCurrencyFormat.getSelectedItem().toString());
            props.setProperty("date.format", jRadioButton1.isSelected() ? "mm/dd/yyyy" : jRadioButton2.isSelected() ? "dd/mm/YYYY" : "dd/mm/YY");
            props.setProperty("time.format", cmdTimeFormat.getSelectedItem().toString());
            props.setProperty("tax.format", jRadioButton4.isSelected() ? "post-tax" : "pre-tax");
            props.setProperty("printer.name", cmbPrinter.getSelectedItem().toString());
            props.setProperty("cash.drawer", cmbCashDrawer.getSelectedItem().toString());
            props.setProperty("alert.lowstock", chkLowStock.isSelected() ? "true" : "false");
            props.setProperty("alert.outofstock", chkOutofStock.isSelected() ? "true" : "false");
            props.setProperty("keyboard.show", chkShowKeyBoard.isSelected() ? "true" : "false");
            props.setProperty("screen.startfull", chkFullScreen.isSelected() ? "true" : "false");
            props.setProperty("discount.overall", chkOverallDisc.isSelected() ? "true" : "false");
            props.setProperty("discount.item", chkItemDisc.isSelected() ? "true" : "false");
            props.setProperty("run.zout", chkZout.isSelected() ? "true" : "false");
            props.setProperty("opendrawer.endsale", chkOpenDrawer.isSelected() ? "true" : "false");
            props.setProperty("allow.holdrecall", chkHoldRecall.isSelected() ? "true" : "false");
            props.setProperty("print.paymodeid", chkPayId.isSelected() ? "true" : "false");
            props.setProperty("print.upc", chkPrintUpc.isSelected() ? "true" : "false");
            props.setProperty("sidetab.show", chkShowTab.isSelected() ? "true" : "false");
            props.setProperty("path.temp", txtTemp.getText());
            File f = new File(System.getProperty("user.dir") + "\\settings.xml");
            OutputStream out = new FileOutputStream(f);
            props.storeToXML(out, "Successfully Saved  on " + new Date().getTime());
        } catch (IOException e) {
            e.getMessage();
        }

    }

